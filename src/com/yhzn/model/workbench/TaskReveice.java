package com.yhzn.model.workbench;

/**
 * 任务接收对象
 * @author liany
 *
 */
public class TaskReveice extends TaskReveiceModel{


	//创建时间str
	private String createDateStr;
	//任务完成时间str
	private String finishDateStr;
	//签收时间str
	private String singedDateStr;
	
	public String getCreateDateStr() {
		return createDateStr;
	}
	public void setCreateDateStr(String createDateStr) {
		this.createDateStr = createDateStr;
	}
	public String getFinishDateStr() {
		return finishDateStr;
	}
	public void setFinishDateStr(String finishDateStr) {
		this.finishDateStr = finishDateStr;
	}
	public String getSingedDateStr() {
		return singedDateStr;
	}
	public void setSingedDateStr(String singedDateStr) {
		this.singedDateStr = singedDateStr;
	}
}
